package tantrany_methods;

public class UserDetail {
	
	private String username, password;
	
	public void setUsername (String X)
	{
		
		username = X;
		
	}
	
	public String getUsername ()
	{
		return username;
	}
	
	public void setPassword (String Y)
	{
		
		password = Y;
		
	}
	
	public String getPassword ()
	{
		return password;
	}
	
	
}
